def relabel_based_on_preds(relabeling_dict, img_paths, GT_paths, preds, threshold, replace_all=False, debug=False):
    paths_found = {"empty": [], "full": [], "use_pred": []}
    for idx, path in enumerate(img_paths):
        match = True
        label_path = GT_paths[idx]
        if path in relabeling_dict["empty"]:
            paths_found["empty"].append(path)
            new_label = np.zeros((512, 512), dtype="uint8")
        elif path in relabeling_dict["full"]:
            paths_found["empty"].append(path)
            new_label = np.ones((512, 512), dtype="uint8")
        elif path in relabeling_dict["use_pred"]:
            paths_found["use_pred"].append(path)
            new_label = ((((preds[idx].astype(np.float32) / 255.0)) > threshold) * 1).astype(np.uint8)
        else:
            match = False
        if match:
            if "_improved.tif" in label_path:
                new_label_path = label_path[:]
            else:
                new_label_path = label_path.replace(".tif", "_improved.tif")
            if replace_all:
                new_label_path = new_label_path.replace("_improved.tif", "_improved_weakly.tif")
            # print(new_label_path)
            if debug:
                f, axarr = plt.subplots(1, 3, figsize=(20, 8))
                print(path, label_path)
                arr_x = tifffile.imread([path.replace("B02.tif", f"B0{k}.tif") for k in [2, 3, 4]])
                if arr_x.shape[0] < 20:
                    arr_x = arr_x.transpose((1, 2, 0))
                img = scale_S2_img(arr_x)
                axarr[0].imshow(img)
                axarr[1].imshow(img)
                axarr[2].imshow(img)
                orig_label = tifffile.imread(label_path)
                axarr[1].imshow(np.ma.masked_where(orig_label != 1, orig_label), cmap="spring", alpha=0.4)
                axarr[1].set_title("OLD LABEL", fontsize=15)
                axarr[2].imshow(np.ma.masked_where(new_label != 1, new_label), cmap="spring", alpha=0.4)
                axarr[2].set_title("NEW LABEL", fontsize=15)
                plt.tight_layout()
                plt.show()
            else:
                tifffile.imwrite(new_label_path, new_label)
    for key in paths_found.keys():
        print(f"{key:10}: Replaced {len(paths_found[key])} labels")
